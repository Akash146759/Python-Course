number1 = 10100000
print("Binary representation:", format(number1, "b"))

x = number1  # Binary:   101
result = x << 2  # Shifts bits left by 2 places
print(result)  # Output: 20 (Binary: 00010100)

x = number1  # Binary: 101
result = x >> 2  # Shifts bits right by 2 places
print(result)  # Output: 3 (Binary: 00000011)

